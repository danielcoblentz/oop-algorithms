#include <iostream>
#include <cmath>  // Include the cmath library to use the pow function

using namespace std;

int main() {
    // Example calculations using pow function
    double result1 = pow(5, 4);  // 5 raised to the power of 4
    double result2 = pow(7, 2);  // 7 raised to the power of 2
    double result3 = pow(10, 3);  // 10 raised to the power of 3
    double result4 = pow(-2, 3);  // -2 raised to the power of 3

    // Output the results
    cout << "5 raised to the power of 4 is: " << result1 << endl;
    cout << "7 raised to the power of 2 is: " << result2 << endl;
    cout << "10 raised to the power of 3 is: " << result3 << endl;
    cout << "(-2) raised to the power of 3 is: " << result4 << endl;

    return 0;
}
